package com.res_detail.model;

public class Res_DetailVO {
	private String rd_no;
	private String res_no;
	private String serv_no;
	private String stype_no;
	private Integer price;
	private String status;
	private Integer score;
	
	public Res_DetailVO() {

	}
	public String getRd_no() {
		return rd_no;
	}
	public void setRd_no(String rd_no) {
		this.rd_no = rd_no;
	}
	public String getRes_no() {
		return res_no;
	}
	public void setRes_no(String res_no) {
		this.res_no = res_no;
	}
	public String getServ_no() {
		return serv_no;
	}
	public void setServ_no(String serv_no) {
		this.serv_no = serv_no;
	}
	public String getStype_no() {
		return stype_no;
	}
	public void setStype_no(String stype_no) {
		this.stype_no = stype_no;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Integer getScore() {
		return score;
	}
	public void setScore(Integer score) {
		this.score = score;
	}
	
	
}
